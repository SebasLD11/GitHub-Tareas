
public class Ejercicio3T04 {

	public static void main(String[] args) {
		// // Declaración e inicialización de la variable N
        int N = 3;

        // Muestra el valor inicial de N
        System.out.println("Valor inicial de N = " + N);

        // Incrementa N en 77 y muestra el resultado
        N += 77; //N = N + 77; //80
        System.out.println("N + 77 = " + N);

        // Decrementa N en 3 y muestra el resultado
        N -= 3;  //N = N - 3; //0
        System.out.println("N - 3 = " + N);

        // Duplica el valor de N y muestra el resultado
        N *= 2;  //N = N * 2; //9
        System.out.println("N * 2 = " + N);
    }
}