
public class Ejercicio2T04 {

	public static void main(String[] args) {
		// Declaración de variables
		 int x = 3;
		 int y = 9;
		 double n = 5;
		 double	m = 7;
		
		 // Operaciones aritmétricas y mostrar resultados en pantalla
		 System.out.println("Suma: " + (x + y) +
		 		"\nResta: " + (x - y) +
		 		"\nMultiplicación: " + (x * y) +
		 		"\nDivisión: " + (x / y) +
		 		"\nResto: " + (x % y));
		 System.out.println("Suma: " + (n + m) +
				"\nResta: " + (n + m) +
		 		"\nMultiplicación: " + (n * m) +
		 		"\nDivisión: " + (n / m) +
		 		"\nResto: " + (n % m));
		 System.out.println("Suma: " + (x + n) +
				 "\nDivisión: " + (y / m) +
				 "\nResto: " + (y % m));
		 System.out.println("Multiplicación: " + (x * 2));
		 System.out.println("Multiplicación: " + (y * 2));		
		 System.out.println("Multiplicación: " + (n * 2));		
		 System.out.println("Multiplicación: " + (m * 2));
		 
		 System.out.println("Suma: " + (x + y + n + m));
		 System.out.println("Multiplicación: " + (x * y * n * m));
		 	 		
	}

}
